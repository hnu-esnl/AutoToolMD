class IDT:
    def __init__(self,IDTName,Dimension):
        self.IDTName = IDTName
        self.Dimension = Dimension

    def setIDTName(self,IDTName):
        self.IDTName = IDTName

    def getIDTName(self):
        return self.IDTName

    def setDimension(self,Dimension):
        self.Dimension = Dimension

    def getDimension(self):
        return self.Dimension